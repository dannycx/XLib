package com.danny.xui.radar;

public class RadarChartConfig {
    private int webColorId;
    private float webLineWidth;
    private float lineWidth;

    public int getWebColorId() {
        return webColorId;
    }

    public void setWebColorId(int webColorId) {
        this.webColorId = webColorId;
    }

    public float getWebLineWidth() {
        return webLineWidth;
    }

    public void setWebLineWidth(float webLineWidth) {
        this.webLineWidth = webLineWidth;
    }

    public float getLineWidth() {
        return lineWidth;
    }

    public void setLineWidth(float lineWidth) {
        this.lineWidth = lineWidth;
    }
}
